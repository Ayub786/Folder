
public class Class {
private String name;
private int sal;
public Class(String name, int sal) {
	super();
	this.name = name;
	this.sal = sal;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getSal() {
	return sal;
}
public void setSal(int sal) {
	this.sal = sal;
}

}
